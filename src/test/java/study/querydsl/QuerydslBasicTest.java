    @Test
    void case문(){
        List<String> result = queryFactory
                .select(member.age
                        .when(10).then("열살")
                        .when(20).then("스무살")
                        .otherwise("기타"))
                .from(member)
                .fetch();

        for (String s : result) {
            System.out.println("s = " + s);
        }

        StringExpression stringExpression = new CaseBuilder()
                .when(member.age.between(0, 20)).then("0~20살")
                .when(member.age.between(21, 30)).then("21~30살")
                .otherwise("기타");

        List<String> result2 = queryFactory
                .select(stringExpression)
                .from(member)
                .fetch();

        for (String s2 : result2) {
            System.out.println("s2 = " + s2);
        }
    }

    /**
     * 임의의 순서로 회원을 출력
     * 복잡한 조건을 변수로 선언해서 select 절, orderBy 절에서 함께 사용
     */
    @Test
    void numberExpression(){
        NumberExpression<Integer> rankPath = new CaseBuilder()
                .when(member.age.between(0, 20)).then(2)
                .when(member.age.between(21, 30)).then(1)
                .otherwise(3);

        List<Tuple> result = queryFactory
                .select(member.username, member.age, rankPath)
                .from(member)
                .orderBy(rankPath.desc())
                .fetch();

        for (Tuple tuple : result) {
            String username = tuple.get(member.username);
            Integer age = tuple.get(member.age);
            Integer rank = tuple.get(rankPath);
            System.out.println("username = " + username + ", age = " + age + ", rank = " + rank);
        }
    }

    /**
     * 상수: Expressions.constant(xxx)
     * 문자 더하기: concat
     */
    @Test
    void constant_concat(){
        Tuple constant = queryFactory
                .select(member.username, Expressions.constant("A"))
                .from(member)
                .fetchFirst();

        String concat = queryFactory
                .select(member.username.concat("_").concat(member.age.stringValue()))
                .from(member)
                .fetchFirst();

        System.out.println("constant = " + constant);
        System.out.println("concat = " + concat);
    }



